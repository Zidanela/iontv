int mock_channelscan_execute_all(struct hdhomerun_device_t *hd, uint32_t channel_map, channelscan_callback_t callback, ...)
{
	va_list callback_ap;
	va_start(callback_ap, callback);
  
	int result = 0;
  int index = 0;
  for (index = 0; index < sizeof mock_callback_array / sizeof *mock_callback_array; index++) { 
    mock_callback_data entry = mock_callback_array[index];
		result = mock_channelscan_execute_callback(callback, callback_ap, entry.type, entry.buffer);
		if (result <= 0) {
			break;
		}
	}
  
	va_end(callback_ap);
	return result;
}

static int mock_channelscan_execute_callback(channelscan_callback_t callback, va_list callback_ap, const char *type, const char *str)
{
	if (!callback) {
		return 1;
	}
	
	va_list ap;
	va_copy(ap, callback_ap);
	int ret = callback(ap, type, str);
	va_end(ap);
  
	return ret;
}

